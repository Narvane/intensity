    fun resolve(key: String): String? {
        val parts = key.split(".")
        if (parts.size != 5) return null
        if (parts[0] != "suggestion") return null
        val type = parts[1]
        val level = parts[2].toIntOrNull() ?: return null
        val index = parts[3].toIntOrNull() ?: return null
        if (parts[4] != "text") return null
        return values[type]?.get(level)?.getOrNull(index - 1)
    }
